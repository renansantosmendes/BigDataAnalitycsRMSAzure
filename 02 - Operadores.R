## Operadores básicos, relacionais e logicos em R

# Operadores básicos
# Soma
4 + 4

# subtração
4 - 4

# multiplicação
4 * 4

# divisão
4 / 4

# potencia
4^2
4**2

# módulo
4 %% 2

# Operadores relacionais
# atribuindo variáveis
a = 7
b = 5  

# operadores
a > 8
a < 8
a <= 8
a >= 8
a == 8
a != 8
  
# Operadores Lógicos
# operador and
(a == 8) & (b == 6)
(a == 7) & (b == 5)
(a == 8) & (b == 5)

# operador or
(a == 8) | (b > 5)

# opeerador not
a > 8
print(! a > 8)

